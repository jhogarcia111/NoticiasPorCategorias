-- Función para obtener estadísticas del admin
CREATE OR REPLACE FUNCTION get_admin_stats(period_days INTEGER DEFAULT 7)
RETURNS JSON AS $$
DECLARE
  result JSON;
BEGIN
  SELECT json_build_object(
    'total_users', (SELECT COUNT(*) FROM profiles),
    'active_users', (SELECT COUNT(*) FROM profiles WHERE updated_at > NOW() - INTERVAL '7 days'),
    'total_posts_published', (SELECT COUNT(*) FROM scheduled_posts WHERE status = 'published' AND created_at > NOW() - (period_days || ' days')::INTERVAL),
    'posts_failed', (SELECT COUNT(*) FROM scheduled_posts WHERE status = 'failed' AND created_at > NOW() - (period_days || ' days')::INTERVAL),
    'error_rate', (
      CASE 
        WHEN (SELECT COUNT(*) FROM scheduled_posts WHERE created_at > NOW() - (period_days || ' days')::INTERVAL) > 0 
        THEN ROUND(
          (SELECT COUNT(*) FROM scheduled_posts WHERE status = 'failed' AND created_at > NOW() - (period_days || ' days')::INTERVAL)::DECIMAL / 
          (SELECT COUNT(*) FROM scheduled_posts WHERE created_at > NOW() - (period_days || ' days')::INTERVAL) * 100, 2
        )
        ELSE 0
      END
    ),
    'active_sources', (SELECT COUNT(*) FROM categories WHERE is_active = true)
  ) INTO result;
  
  RETURN result;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

